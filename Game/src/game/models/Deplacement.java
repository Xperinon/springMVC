package game.models;

public class Deplacement {

	private int deplacementX;
	private int deplacementY;
	private int deplacementZ;
	private int vitesseX;
	private int vitesseY;
	private int vitesseZ;
	
	
	
	
	
	

	public int getDeplacementX() {
		return deplacementX;
	}
	public void setDeplacementX(int deplacementX) {
		this.deplacementX = deplacementX;
	}
	public int getDeplacementY() {
		return deplacementY;
	}
	public void setDeplacementY(int deplacementY) {
		this.deplacementY = deplacementY;
	}
	public int getDeplacementZ() {
		return deplacementZ;
	}
	public void setDeplacementZ(int deplacementZ) {
		this.deplacementZ = deplacementZ;
	}
	public int getVitesseX() {
		return vitesseX;
	}
	public void setVitesseX(int vitesseX) {
		this.vitesseX = vitesseX;
	}
	public int getVitesseY() {
		return vitesseY;
	}
	public void setVitesseY(int vitesseY) {
		this.vitesseY = vitesseY;
	}
	public int getVitesseZ() {
		return vitesseZ;
	}
	public void setVitesseZ(int vitesseZ) {
		this.vitesseZ = vitesseZ;
	}
}
